{



}

//==========================================================
// 生成
//==========================================================
CUI *CUI::Create(void)
{
	CUI *pUI = nullptr;

	pUI = new CUI;

	if (pUI != nullptr)
	{
		// 初期化処理
		pUI->Init();
	}	

	return pUI;
}